# Notes of an Insomniac
  
Of the many extraordinary qualities I possess, asking stupid questions is the one which
I'm really proud of. This has lead to the impression, amongst those around me, that I am beyond 
reproach & probably should be left to being myself.

In the spirit of disorganized inquiry, this page will contain posts on academic stuff.
These are the things usually explained using in classrooms using classical teaching techniques like
'proof by intimidation' & get brushed under the gradesheets of students who couldn't care less about
the subject.

